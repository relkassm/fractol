#include "libft.h"

void	*ft_memcpy(void *to, const void *from, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *)to)[i] = ((char *)from)[i];
		i++;
	}
	return (to);
}
